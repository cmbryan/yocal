const SIX_PSALMS_INTRO = "## Selection from the Six Psalms";
const WEEKDAY_SECTION_END_MARKER = "## Prayer to your Guardian Angel";

const WEEKDAY_PSALM_BY_DAY: Record<number, string | null> = {
	0: null,
	1: "### Psalm 3",
	2: "### Psalm 37",
	3: "### Psalm 62",
	4: "### Psalm 87",
	5: "### Psalm 102",
	6: "### Psalm 142",
};

function escapeRegExp(value: string): string {
	return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function getWeekdayPsalmSection(psalmName: string): string {
	const match = MORNING_PRAYER_TEXT.match(
		new RegExp(
			`${escapeRegExp(psalmName)}(?:\\s+\\([^\n]+\\))?\\n+[\\s\\S]*?(?=\\n\\n)`,
		),
	);

	return match?.[0]?.trim() ?? "";
}

export function getMorningPrayerTextForDate(date: Date): string {
	const psalmName = WEEKDAY_PSALM_BY_DAY[date.getDay()];
	const beforeWeeklySection = MORNING_PRAYER_TEXT.split(`${SIX_PSALMS_INTRO}\n\n`)[0]?.trim() ?? MORNING_PRAYER_TEXT.trim();
	const afterWeeklySection = MORNING_PRAYER_TEXT.includes(`\n\n${WEEKDAY_SECTION_END_MARKER}`)
		? `${WEEKDAY_SECTION_END_MARKER}${MORNING_PRAYER_TEXT.split(`\n\n${WEEKDAY_SECTION_END_MARKER}`)[1] ?? ""}`.trim()
		: "";

	const parts = [beforeWeeklySection, SIX_PSALMS_INTRO];

	if (psalmName) {
		const weekdayPsalm = getWeekdayPsalmSection(psalmName);
		if (weekdayPsalm) {
			parts.push(weekdayPsalm);
		}
	}

	if (afterWeeklySection) {
		parts.push(afterWeeklySection);
	}

	return parts.join("\n\n");
}
