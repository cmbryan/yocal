export function getTypikaSections(): { beforeReadings: string; afterReadings: string } {
  const start = TYPIKA_TEXT.indexOf(TYPIKA_READING_SECTION_INTRO);
  const end = TYPIKA_TEXT.indexOf(TYPIKA_READING_SECTION_END, start + TYPIKA_READING_SECTION_INTRO.length);

  if (start === -1 || end === -1 || end <= start) {
    return {
      beforeReadings: TYPIKA_TEXT.trim(),
      afterReadings: "",
    };
  }

  return {
    beforeReadings: TYPIKA_TEXT.slice(0, start + TYPIKA_READING_SECTION_INTRO.length).trim(),
    afterReadings: TYPIKA_TEXT.slice(end).trim(),
  };
}
